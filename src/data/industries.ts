export const getIndustryById = (id: IndustryId): Industry | undefined => {
  return industries.find((i) => i.id === id);
};

export const getIndustryRegulations = (industryId: IndustryId): string[] => {
  const industry = getIndustryById(industryId);
  return industry?.regulations || [];
};

export const getIndustryMetrics = (industryId: IndustryId): string[] => {
  const industry = getIndustryById(industryId);
  return industry?.keyMetrics || [];
};
